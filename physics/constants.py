import scipy.constants

me = scipy.constants.m_e #Electron mass in kg
mp = scipy.constants.m_p #Proton mass in kg
q = scipy.constants.e #Elementary charge in C
G = scipy.constants.G #Gravitational constant in N*m^2/kg^2
c = scipy.constants.c #Speed of light in m/s
epsilon0 = scipy.constants.epsilon_0 #Electric constant in F/m
mu0 = scipy.constants.mu_0 #Magnetic constant in T*m/A
Planck = scipy.constants.h #Planck constant in J*s
hbar = scipy.constants.hbar #Reduced Planck constant in J*s
kb = scipy.constants.k #Boltzmann constant in J/K
NA = scipy.constants.N_A #Avagadro's number
